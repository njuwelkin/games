// opcodeToString 将操作码转换为可读的字符串描述
func opcodeToString(opcode mkf.WORD) string {
	if desc, ok := opcodeMap[opcode]; ok {
		return "(" + desc + ")"
	}
	return "(未知操作码)"
}
